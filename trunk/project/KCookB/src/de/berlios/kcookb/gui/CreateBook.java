    private void jbtnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBrowseActionPerformed
        JFileChooser jfc = new JFileChooser(startPath);

        //TODO: solve filter and view problems
        //jfc.setFileFilter(new KCBFileFilter());
        //jfc.setFileView(new KCBFileView());

        jfc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        if (jfc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            jtfBookFolder.setText(jfc.getSelectedFile().getAbsolutePath().trim());
        }
    }//GEN-LAST:event_jbtnBrowseActionPerformed

